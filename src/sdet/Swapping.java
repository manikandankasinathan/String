package sdet;

public class Swapping {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("before swapping"+a);
		System.out.println("After swapping"+b);
		int c;
		c=a;
		a=b;
		b=c;
		System.out.println("after wwapping"+a);
		System.out.println("Afetr swapping"+b);
				
	}

}
